#include<stdio.h>
void test()
{
    int a[2][2] = {55, 66, 77, 88};
    int b[2] = {11, 22};
    int(*p)[2];
    p = &b;
    printf("(00)=>%d\n", **p);       // ----(1)
    printf("(04)=>%d\n", p[0][0]);   // ---(2)
    printf("(08)=>%d\n", **(p + 1)); // ----(3)
    p = &a[0];
    printf("(11)=>%d\n", *(*p + 1)); // ----(4)
    printf("(13)=>%d\n", (*p)[1]);   // -----(5)
    p = a;
    printf("(31)=>%d\n", *(*(p + 1) + 1)); // -----(6)
    printf("(32)=>%d\n", (*(p + 1))[0]);   // -----(7)
}
int main(){
    test();
}